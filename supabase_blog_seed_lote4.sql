## O papel do garupa

O passageiro não é peso morto — ele participa:

- **Acompanhe o corpo do piloto nas curvas**, sem fazer força contrária. Olhe por cima do ombro do piloto, no sentido da curva.
- **Não faça movimentos bruscos.** Mexer de repente desestabiliza a moto.
- **Segure-se firme** (no piloto, na alça ou no tanque), principalmente em freadas e arrancadas.
- **Suba e desça só quando o piloto avisar**, com a moto firme e os dois pés do piloto no chão.

## Comunicação: combine sinais

Antes de sair, combinem sinais simples — um toque no ombro pra "vamos parar", apontar pra "olha aquilo". Intercomunicador (Bluetooth no capacete) resolve de vez e deixa a viagem muito mais agradável.

## Conforto pra aguentar a estrada

- **Garupa também precisa de equipamento completo:** capacete, jaqueta, calça, luva e bota. Proteção não é só do piloto.
- **Apoio de pé e encosto (sissy bar)** ajudam muito em viagem longa.
- **Pare com mais frequência.** O garupa cansa de jeito diferente — alongue e descanse.
- **Distribua a bagagem** pensando no espaço do passageiro. Veja como [escolher as malas](/blog/malas-e-bagageiro-para-viagem-de-moto).

